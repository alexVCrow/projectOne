public class UserDto {

    private String login;
    private String  name;
    private String age;

    public String getLogin() {
        return login;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
